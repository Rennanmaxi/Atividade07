package basicas;

public class Boleto {
	private int codigoBoleto;
	private date dataPagamento;
	private double valorPagor;
	
	public int getCodigoBoleto() {
		return codigoBoleto;
	}
	public void setCodigoBoleto(int codigoBoleto) {
		this.codigoBoleto = codigoBoleto;
	}
	public date getDataPagamento() {
		return dataPagamento;
	}
	public void setDataPagamento(date dataPagamento) {
		this.dataPagamento = dataPagamento;
	}
	public double getValorPagor() {
		return valorPagor;
	}
	public void setValorPagor(double valorPagor) {
		this.valorPagor = valorPagor;
	}

	
	

}
